// 当前语言
let currentLang = localStorage.getItem('tarot-lang') || 'zh';

// 获取翻译文本
function t(key) {
    const langData = translations[currentLang];
    return langData[key] || key;
}

// 切换语言
function toggleLanguage() {
    currentLang = currentLang === 'zh' ? 'en' : 'zh';
    localStorage.setItem('tarot-lang', currentLang);
    applyTranslations();
}

// 应用翻译到页面
function applyTranslations() {
    // 更新语言按钮文本
    const langBtn = document.getElementById('langBtn');
    if (langBtn) {
        langBtn.textContent = t('langBtn');
    }

    // 更新所有带有 data-i18n 属性的元素
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const value = t(key);
        if (value) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = value;
            } else {
                el.innerHTML = value;
            }
        }
    });

    // 更新所有带有 data-i18n-text 属性的元素（纯文本，不解析HTML）
    document.querySelectorAll('[data-i18n-text]').forEach(el => {
        const key = el.getAttribute('data-i18n-text');
        const value = t(key);
        if (value) {
            el.textContent = value;
        }
    });

    // 更新页面标题
    const pageTitle = document.querySelector('title');
    if (pageTitle && pageTitle.getAttribute('data-i18n-title')) {
        const titleKey = pageTitle.getAttribute('data-i18n-title');
        const titleValue = t(titleKey);
        if (titleValue) {
            pageTitle.textContent = titleValue;
        }
    }

    // 更新 HTML lang 属性
    document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : 'en';

    // 触发自定义事件，通知页面其他脚本语言已切换
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: currentLang } }));
}

// 初始化
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
